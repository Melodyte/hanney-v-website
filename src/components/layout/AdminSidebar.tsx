const navItems: NavItem[] = [
  { href: "/admin", label: "Dashboard", icon: <DashboardIcon /> },
  { href: "/admin/portfolio", label: "Portfolio", icon: <ImageIcon /> },
  { href: "/admin/products", label: "Products", icon: <ShoppingBagIcon /> },
  { href: "/admin/bookings", label: "Bookings", icon: <CalendarIcon /> },
  { href: "/admin/testimonials", label: "Testimonials", icon: <StarIcon /> },
  { href: "/admin/blog", label: "Blog", icon: <FileTextIcon /> },
  { href: "/admin/subscribers", label: "Subscribers", icon: <MailIcon /> },
  { href: "/admin/settings", label: "Settings", icon: <SettingsIcon /> },
];

export default function AdminSidebar() {
  const [collapsed, setCollapsed] = useState(false);
  const pathname = usePathname();

  const isActive = (href: string) => {
    if (href === "/admin") {
      return pathname === "/admin";
    }
    return pathname.startsWith(href);
  };

  return (
    <aside
      className={`fixed left-0 top-0 h-full bg-neutral-950 border-r border-neutral-800 transition-all duration-300 z-40 flex flex-col ${
        collapsed ? "w-16" : "w-64"
      }`}
      aria-label="Admin navigation"
    >
      {/* Logo / Brand */}
      <div className="flex items-center gap-2 px-4 h-16 border-b border-neutral-800 shrink-0">
        <Image
          src="/images/Logo/photo_2026-05-21 06.13.30.jpeg"
          alt="Hanney-V logo"
          width={32}
          height={32}
          className="h-8 w-8 object-contain rounded-full shrink-0"
        />
        {!collapsed && (
          <span className="font-heading text-lg font-bold text-white truncate">
            Hanney-V
          </span>
        )}
      </div>

      {/* Navigation */}
      <nav className="flex-1 py-4 overflow-y-auto">
        <ul className="space-y-1 px-2">
          {navItems.map((item) => (
            <li key={item.href}>
              <Link
                href={item.href}
                className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                  isActive(item.href)
                    ? "bg-gold/10 text-gold"
                    : "text-neutral-400 hover:text-white hover:bg-neutral-800"
                } ${collapsed ? "justify-center" : ""}`}
                title={collapsed ? item.label : undefined}
              >
                <span className="shrink-0">{item.icon}</span>
                {!collapsed && <span>{item.label}</span>}
              </Link>
            </li>
          ))}
        </ul>
      </nav>

      {/* Collapse Toggle */}
      <div className="border-t border-neutral-800 p-2 shrink-0">
        <button
          onClick={() => setCollapsed(!collapsed)}
          className="w-full flex items-center justify-center gap-2 px-3 py-2 text-neutral-400 hover:text-white rounded-lg hover:bg-neutral-800 transition-colors text-sm"
          aria-label={collapsed ? "Expand sidebar" : "Collapse sidebar"}
        >
          {collapsed ? <ChevronRightIcon /> : <ChevronLeftIcon />}
          {!collapsed && <span>Collapse</span>}
        </button>
      </div>
    </aside>
  );
}
